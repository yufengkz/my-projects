<template>
  <div id="app">
    <router-view v-if="isRouterAlive"></router-view>
    <backto-top/>
  </div>
</template>

<script>

export default {
  name: 'App',
  provide() {
    return {
      reload: this.reload
    }
  },
  data() {
    return {
      isRouterAlive: true
    }
  },
  created() {
    // common 获取map中心列表
    this.$store.dispatch('GetMAPList')
  },
  methods: {
    reload() {
      this.isRouterAlive = false
      this.$nextTick(() => {
        this.isRouterAlive = true
      })
    }
  }
}
</script>

